export function getLogoUrl(team) {
  if (team.logo) return team.logo;
  if (LOGO_OVERRIDES[team.n]) return LOGO_OVERRIDES[team.n];
  return null;
}

export function renderLogo(wrapEl, flagEl, team) {
  const url = getLogoUrl(team);
  wrapEl.innerHTML = '';
  if (url) {
    const img = document.createElement('img');
    img.style.cssText = 'width:48px;height:48px;object-fit:contain;display:block;';
    img.src = url;
    img.alt = team.n;
    img.loading = 'lazy';
    img.onerror = () => {
      wrapEl.innerHTML = `<span class="team-logo-fallback" style="font-size:2rem;line-height:1">${team.f||'🌐'}</span>`;
    };
    wrapEl.appendChild(img);
  } else {
    wrapEl.innerHTML = `<span class="team-logo-fallback" style="font-size:2rem;line-height:1">${team.f||'🌐'}</span>`;
  }
}

export async function fetchTeamsData() {
  try {
    const res = await fetch('./teams.json');
    if (!res.ok) throw new Error("Teams data not found");
    return await res.json();
  } catch (err) {
    console.error(err);
    return [];
  }
}

